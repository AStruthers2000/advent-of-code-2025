    std::vector<Range> m_ranges;
    std::vector<std::uint64_t> m_ids;

    std::optional<Range> merge_ranges_if_overlap(Range const& r1, Range const& r2);
    std::vector<Range> merge_all_ranges(std::vector<Range> all_ranges);
